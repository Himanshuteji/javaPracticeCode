public class Printing {
    public static void main(String[] args) {
        int a,b;
        a=10;
        b=20;
        System.out.println(a+b+"Hello");
        System.out.println(a+"Hello"+b);
        System.out.println("Hello World"+a+b);
        System.out.println("Hello World"+(a+b));
        System.out.println(a+" "+b+"Hello World");


    }
}
